def demonstrate_operators():
    print("\n=== Operators ===")
    a = 10
    b = 3

    # Arithmetic operators
    print(f"Addition: {a + b}")
    print(f"Subtraction: {a - b}")
    print(f"Multiplication: {a * b}")
    print(f"Division: {a / b}")

    # Comparison operators
    print(f"Is a > b? {a > b}")
    print(f"Is a == b? {a == b}")

if __name__ == "__main__":
    demonstrate_operators()
